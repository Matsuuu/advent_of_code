public class Overlap {
    private int overlapMinute;
    private int overlapOccurance;

    public Overlap(int overlapMinute, int overlapOccurance) {
        this.overlapMinute = overlapMinute;
        this.overlapOccurance = overlapOccurance;
    }

    public int getOverlapMinute() {
        return overlapMinute;
    }

    public void setOverlapMinute(int overlapMinute) {
        this.overlapMinute = overlapMinute;
    }

    public int getOverlapOccurance() {
        return overlapOccurance;
    }

    public void setOverlapOccurance(int overlapOccurance) {
        this.overlapOccurance = overlapOccurance;
    }
}
